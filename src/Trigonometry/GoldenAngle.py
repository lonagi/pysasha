#for example, the golden angle is the angle separating the florets on a sunflower.

GoldenAngle = 360*(1-(1.236067977499789716775759127427135908658201562710016/2))
print(GoldenAngle) # 137. degrees
GoldenAngle/=360
print(GoldenAngle) # 0.38 the fraction
GoldenAngle*=2*3.14159
